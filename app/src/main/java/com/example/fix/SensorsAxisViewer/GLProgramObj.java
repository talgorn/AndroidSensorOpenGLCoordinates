package com.example.fix.SensorsAxisViewer;
import android.opengl.GLES20;

//Just a small utility class to create a GL program
//Takes Shader objects as input parameters
public class GLProgramObj {
    private int mProgram;

    public GLProgramObj (ShaderObj vertexShader, ShaderObj fragmentShader) {
        mProgram = create(vertexShader, fragmentShader);
    }

    public int create(ShaderObj v, ShaderObj f) {
        int programHandle = GLES20.glCreateProgram();

        if (programHandle != 0)
        {
            // Bind the vertex shader to the program.
            GLES20.glAttachShader(programHandle, v.getShaderHandler());

            // Bind the fragment shader to the program.
            GLES20.glAttachShader(programHandle, f.getShaderHandler());

            // Bind attributes
            GLES20.glBindAttribLocation(programHandle, 0, "a_Position");
            GLES20.glBindAttribLocation(programHandle, 1, "a_Color");

            // Link the two shaders together into a program.
            GLES20.glLinkProgram(programHandle);

            // Get the link status.
            final int[] linkStatus = new int[1];
            GLES20.glGetProgramiv(programHandle, GLES20.GL_LINK_STATUS, linkStatus, 0);

            // If the link failed, delete the program.
            if (linkStatus[0] == 0)
            {
                GLES20.glDeleteProgram(programHandle);
                programHandle = 0;
            }
        }
        if (programHandle == 0)
        {
            throw new RuntimeException("Error creating program.");
        }
        return programHandle;
    }

}












